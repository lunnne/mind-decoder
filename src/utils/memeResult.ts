function pick(pool: string[]): string {
  return pool[Math.floor(Math.random() * pool.length)];
}

export function getMemeResult(scorePercent: number): string {
  if (scorePercent === 100) return pick(MESSAGES.perfect);
  if (scorePercent >= 80)   return pick(MESSAGES.great);
  if (scorePercent >= 60)   return pick(MESSAGES.decent);
  if (scorePercent >= 40)   return pick(MESSAGES.half);
  if (scorePercent >= 20)   return pick(MESSAGES.low);
  return                         pick(MESSAGES.zero);
}
